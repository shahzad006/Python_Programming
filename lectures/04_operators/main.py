#! ---------------------------------------------------------------------------- #
#?                            1. Arithmetic Operators                           #
#! ---------------------------------------------------------------------------- #

num_1:int = 5
num_2:int = 2


print(num_1 + num_2)            # Addition
print(num_1 - num_2)            # Subtraction
print(num_1 * num_2)            # Multiply
print(num_1 / num_2)            # Divide
print(num_1 // num_2)           # Floor Divide
print(num_1 % num_2)            # Modulus (Remainder)Divide
print(num_1 ** num_2)           #Power



#! ---------------------------------------------------------------------------- #
