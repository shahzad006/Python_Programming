#! ---------------------------------------------------------------------------- #
#?                            3. Comparison Operators                           #
#! ---------------------------------------------------------------------------- #


num_1 = 7
num_2 = 4

print(num_1 == num_2)   # Equal
print(num_1 != num_2)   # Not Equal
print(num_1 > num_2)    # Greater
print(num_1 < num_2)    # Less
print(num_1 >= num_2)   # Greater Equal
print(num_1 <= num_2)   # Less Equal



#! ---------------------------------------------------------------------------- #
#?                             4. Logical Operators                             #
#! ---------------------------------------------------------------------------- #

num_1 = 7
num_2 = 4

print(num_1 > 5 and num_2 < 5)   # Logical AND
print(num_1 > 5 or num_2 < 5)    # Logical OR
print(not(num_1 > 5))             # Logical NOT




