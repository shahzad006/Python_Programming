
#? -------------------------------- Data types -------------------------------- #

# Number types
x = 10          # int
pi = 3.14       # float
z = 2 + 5j      # complex
print(x)
print(pi)
print(z)

# ---------------------------------------------------------------------------- #

# Text type
name = "Ali"    # str
print(name)



# ---------------------------------------------------------------------------- #




# Collection types
items = [1, 2]              # list
coordinates = (4, 5)        # tuple
unique_ids = {101, 102}     # set
student = {"name": "Sara"}  # dict
print(items)
print(coordinates)
print(unique_ids)
print(student)

# ---------------------------------------------------------------------------- #


# Boolean
is_valid = True             # bool
print(is_valid)

# ---------------------------------------------------------------------------- #

# Special
nothing = None              # NoneType
print(nothing)
